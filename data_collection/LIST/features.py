# LIST
# feature
# 1.list is ordered.
list1=[4,7,9,0,1,3]
print(list1)
# 2.list is heterogeneous.
list2=[4,7,'hello',0,1.7,3,True]
print(list2)
# 3.list allow duplication.
list3=[4,1.7,'hello',0,1.7,3,True,False,4]
print(list3)
# 4.list is mutable.
# append elements to list
# 5.list support indexing and slicing.
hazel=[1,3,4,6,7]
print(hazel[3])
print(hazel[3:5])
# 6.list support nesting.
l=[1,3,5,6,[4,6,[4,6]]]
print(l[3])
print(l)
# 7.elementvise updation is possible.
l=[123,45346,8787,574]
l[2]=10000
print(l)
l=[1,2,3,4,5,6,7]
l[:3]=[9,77,5,8]
print(l)